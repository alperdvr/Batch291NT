package Notlar;

public class methodCatagori {
    public static void main(String[] args) {


//        Java String metotlarının kategorilere ayrılmış bir listesi:

//        1. Karakter ve Dize Erişim Metotları
//        charAt(int index): Belirtilen dizindeki karakteri döndürür.
//        substring(int beginIndex, int endIndex): Belirtilen dizinler arasındaki alt diziyi döndürür.
//        indexOf(String str): Belirtilen alt dizenin ilk göründüğü dizini döndürür.

//        2. Karşılaştırma ve Kontrol Metotları
//        equals(Object anObject): İki String'in eşit olup olmadığını kontrol eder.
//        equalsIgnoreCase(String anotherString): Büyük/küçük harf dikkate almadan iki String'in eşit olup olmadığını kontrol eder.
//        contains(CharSequence s): Belirtilen karakter dizisinin String içinde olup olmadığını kontrol eder.
//                startsWith(String prefix): String'in belirtilen önek ile başlayıp başlamadığını kontrol eder.
//        endsWith(String suffix): String'in belirtilen sonek ile bitip bitmediğini kontrol eder.
//        isEmpty(): String'in boş olup olmadığını kontrol eder.

//        3. Dönüştürme ve Manipülasyon Metotları
//        concat(String str): İki String'i birleştirir.
//        replace(char oldChar, char newChar): Belirtilen eski karakterin yerine yeni karakteri koyar.
//                replaceAll(String regex, String replacement): Belirtilen düzenli ifadeye uyan tüm alt dizeleri değiştirilmiş olanla değiştirir.
//        toLowerCase(): String'i küçük harfe dönüştürür.
//        toUpperCase(): String'i büyük harfe dönüştürür.
//        trim(): String'in başındaki ve sonundaki boşlukları kaldırır.

//        4. Uzunluk ve Yapı Metotları
//        length(): String'in uzunluğunu döndürür.
//        split(String regex): String'i belirtilen düzenli ifadeye göre böler.

//        5. Formatlama ve Diğer Yardımcı Metotlar
//        format(String format, Object... args): Biçimlendirilmiş bir String döndürür.
//        valueOf(Object obj): Farklı veri tiplerini String'e dönüştürür.

    }
}
